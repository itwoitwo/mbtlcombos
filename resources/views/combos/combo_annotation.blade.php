            <h5> コンボ投稿について</h5>
            <div>
            <p>
                レシピ欄は様々な状況に対応出来るよう、基本的に自由に記述することが可能ですが技の繋ぎは半角の「>」を使用してください。<br>
                タグや備考欄に書き込む内容は公序良俗に違反せず、他の人が不快にならないような表現をするよう努めてください。<br>
