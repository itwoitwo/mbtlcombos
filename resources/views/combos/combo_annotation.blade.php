            <h5>その他の用語</h5>
            <ul>
                <li>BE：ブローバックエッジ（溜め攻撃）</li>
                <li>AD:アークドライブ</li>
                <li>LA:ラストアーク</li>
                <li>MDc:ムーンドライブキャンセル</li>
                <li>jc：ジャンプキャンセル（攻撃をジャンプでキャンセル）</li>
                <li>hjc：ハイジャンプキャンセル（攻撃をハイジャンプでキャンセル）<ul><li>空中で一瞬だけ9方向に入力することで、通常より高く飛べる。</li></ul></li>
                <li>dl.：ディレイ。技を遅らせて出すこと。<ul><li>例：2A&gt;dl.2Bの場合、2Aの後に一瞬待ってから2Bを出す。</li></ul></li>
                <li>ムーンアイコン：ムーンスキル1つ使用で1カウント。</li>
                <li>発売後、ゲーム内表記を参考に修正予定</li>
